def calculate_addition(first_addend: float, 
                       second_addend: float) -> float:
    calculation_sum = first_addend + second_addend
    return calculation_sum

def calculate_subtraction(minuend_value: float, 
                          subtrahend_value: float) -> float:
    result_difference = minuend_value - subtrahend_value
    return result_difference
